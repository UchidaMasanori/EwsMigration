/// <summary>
/// 親回路パラメータ・入力パラメータから、内部変換テーブルにより下流回路の
/// 主回路パラメータ(<see cref="MainCircuitParameter"/> = struct MCPRMS)を
/// 決定する変換ユーティリティ。
///
/// 【C原典】toku/sekkei/src/Fyss14.c:1437 mcprmcnv(pprm, iprm, oprm)。
///
/// 変換仕様(C原典コメントより):
///   - 変換テーブルの 0 という数字は「使用していない」という意味を持つ。
///   - 変換テーブルの電圧配列の 1 という数字は「指定あり」という意味を持つ。
///   - テーブル検索は親回路パラメータ及び入力データをそれぞれ一時データへ
///     コピーし、電圧が設定されていれば 1、そうでなければ 0 を設定して
///     キーデータを生成し、これらと変換テーブルデータを memcmp で比較する。
///   - 設定される電圧は入力データ・親データの順に優先順位が高い(ただし
///     C 原典の実装では両分岐とも親データ pprm->v[i] を採用している)。
///   - テーブル終端はテーブル中の親回路データの相(ph)が 0 か否かで判定する。
///
/// 外部データ(ISAM/マスタ)には依存しない純粋なテーブル照合であり、
/// C 原典の memcmp による完全一致・先頭一致採用と副作用(oprm への設定)を
/// そのまま再現する。要素数は常に 3(v[0]=v1, v[1]=v2, v[2]=v3)を前提とする。
/// </summary>
public static class MainCircuitParameterConverter
{
    /// <summary>
    /// 変換テーブルのキー/値となる主回路パラメータ 1 件。
    /// 【C原典】struct MCPRMS { SHORT ph, wr, p, v[3], vkbn; }。
    /// 値等価(record struct)により C の memcmp(7 個の SHORT の一致)を再現する。
    /// </summary>
    private readonly record struct Mcprms(
        short Ph, short Wr, short P, short V0, short V1, short V2, short Vkbn);

    /// <summary>
    /// 変換テーブル 1 行。【C原典】prmtable[] の { tp(親), ti(入力), to(下流) }。
    /// </summary>
    private readonly record struct Row(Mcprms Parent, Mcprms Input, Mcprms Output);

    /// <summary>
    /// 親回路パラメータ <paramref name="parent"/> と入力パラメータ
    /// <paramref name="input"/> から変換テーブルを照合し、一致すれば下流回路の
    /// パラメータを <paramref name="output"/> へ設定する。
    /// 【C原典】Fyss14.c:1437 mcprmcnv。正常終了(一致)は 0、非該当は -1 を返す。
    /// </summary>
    /// <param name="parent">親回路パラメータ(pprm)。破壊的更新はしない。</param>
    /// <param name="input">入力パラメータ(iprm)。破壊的更新はしない。</param>
    /// <param name="output">変換された下流回路パラメータ(oprm)。一致時のみ設定。</param>
    /// <returns>一致すれば 0、非該当は -1。</returns>
    public static int ConvertParameter(
        MainCircuitParameter parent, MainCircuitParameter input, MainCircuitParameter output)
    {
        ArgumentNullException.ThrowIfNull(parent);
        ArgumentNullException.ThrowIfNull(input);
        ArgumentNullException.ThrowIfNull(output);

        // 【C原典】親回路パラメータのキーデータ設定。tp.v[i] = tp.v[i] ? 1 : 0。
        Mcprms tp = new(
            parent.Phase, parent.WireType, parent.Pole,
            (short)(parent.Voltage[0] != 0 ? 1 : 0),
            (short)(parent.Voltage[1] != 0 ? 1 : 0),
            (short)(parent.Voltage[2] != 0 ? 1 : 0),
            parent.AcDcKind);

        // 【C原典】入力電圧パラメータのキーデータ設定。ti.v[i] = ti.v[i] ? 1 : 0。
        Mcprms ti = new(
            input.Phase, input.WireType, input.Pole,
            (short)(input.Voltage[0] != 0 ? 1 : 0),
            (short)(input.Voltage[1] != 0 ? 1 : 0),
            (short)(input.Voltage[2] != 0 ? 1 : 0),
            input.AcDcKind);

        // 【C原典】DC電圧だった場合のキーデータ補正。
        //   if(tp.vkbn){ tp.ph=1; tp.wr=2; tp.p=2; ti.vkbn=1; }
        //   tp.vkbn 自体は元の非0値のまま(memcmp 忠実のため正規化しない)。
        if (tp.Vkbn != 0)
        {
            tp = tp with { Ph = 1, Wr = 2, P = 2 };
            ti = ti with { Vkbn = 1 };
        }

        // 【C原典】oprm->v[i] は memcpy 後に pprm->v[i](親の実電圧)で上書きされる。
        // 出力書き込み前に親の実電圧を控える(C では oprm と pprm は別実体)。
        short pv0 = parent.Voltage[0];
        short pv1 = parent.Voltage[1];
        short pv2 = parent.Voltage[2];

        // 【C原典】キーによるテーブル検索。for( i=0 ; prmtable[i].tp.ph ; i++ )。
        // 終端行 {0,...} は本テーブルに含めず、非該当時に -1 を返すことで再現する。
        foreach (Row row in ConversionTable)
        {
            if (tp == row.Parent && ti == row.Input)
            {
                // 【C原典】memcpy(oprm,&prmtable[i].to,...); で to をコピー。
                Mcprms to = row.Output;
                output.Phase = to.Ph;
                output.WireType = to.Wr;
                output.Pole = to.P;

                // 【C原典】oprm->v[i] = oprm->v[i] ? (iprm->v[i] ? pprm->v[i] : pprm->v[i]) : 0;
                //   両分岐とも pprm->v[i](親の実電圧)。to の指定フラグが立つ位置に親電圧を格納。
                output.Voltage[0] = (short)(to.V0 != 0 ? pv0 : 0);
                output.Voltage[1] = (short)(to.V1 != 0 ? pv1 : 0);
                output.Voltage[2] = (short)(to.V2 != 0 ? pv2 : 0);
                output.AcDcKind = to.Vkbn;

                return 0; // 【C原典】return(prmtable[i].tp.ph ? 0 : -1); の一致側。
            }
        }

        // 【C原典】終端に到達 = 非該当 → -1。
        return -1;
    }

